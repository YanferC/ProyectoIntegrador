    private void jbtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAgregarActionPerformed
        if (!validarEntradas()) {
            return;
        }

        // Obtener datos de los campos de texto
        String numero_Identificacion = jtfNumeroIdentificacion.getText();
        String nombreCompleto = jtfNombreCompleto.getText();
        String direccion = jtfDireccion.getText();
        String telefono = jtfTelefono.getText();
        String correo = jtfCorreoElectronico.getText();

        // Crear un objeto Asesor
        Asesor nuevoAsesor = new Asesor(numero_Identificacion, nombreCompleto, direccion, telefono, correo);

        // Agregar cliente utilizando el controlador
        if (crearAsesor.armarCrud().Crear(nuevoAsesor)) {
                JOptionPane.showMessageDialog(this, "Asesor agregado exitosamente.");
                // Actualizar la tabla en el formulario Asesor
                formularioAsesor.actualizarTabla();
                datosAsesorVista datosAsesor = new datosAsesorVista();
                datosAsesor.setVisible(true);

                this.dispose(); // Cerrar el formulario
        } else {
            JOptionPane.showMessageDialog(this, "Error al agregar Asesor.");
        }
    }//GEN-LAST:event_jbtAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        // Código para actualizar el Asesor
        String numero_identificacion = jtfNumeroIdentificacion.getText();
        String nombre = jtfNombreCompleto.getText();
        String direccion = jtfDireccion.getText();
        String telefono = jtfTelefono.getText();
        String correo = jtfCorreoElectronico.getText();

        Asesor actualizarAsesor = new Asesor(numero_identificacion, nombre, direccion, telefono, correo);
        // Llamar al controlador para actualizar
        if (crearAsesor.armarCrud().Actualizar(actualizarAsesor)) {
            JOptionPane.showMessageDialog(this, "Asesor actualizado correctamente.");
            // Cerrar el formulario después de actualizar
            formularioAsesor.actualizarTabla();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar el Asesor.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnActualizarActionPerformed

