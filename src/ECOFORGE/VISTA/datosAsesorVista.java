    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Verificar que se ha seleccionado un registro en la tabla
        int filaSeleccionada = tAsesor.getSelectedRow();

        if (filaSeleccionada == -1) {
            // Si no se ha seleccionado ninguna fila
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un asesor para eliminar.");
            return;
        }

        // Obtener la cédula del asesor seleccionado (asumiendo que la cédula está en la primera columna)
        String numero_Identificacion = (String) tAsesor.getValueAt(filaSeleccionada, 0);

        // Confirmar la eliminación
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar este Asesor?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {

            // Intentar eliminar el asesor
            if (crearAsesor.armarCrud().Eliminar(numero_Identificacion, null)) {
                JOptionPane.showMessageDialog(this, "Asesor eliminado correctamente.");

                // Actualizar la tabla después de eliminar el asesor
                actualizarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el Asesor.");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // Verificar que se ha seleccionado un registro en la tabla
        int filaSeleccionada = tAsesor.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un asesor para actualizar.");
            return;
        }

        // Obtener los datos del asesor seleccionado
        String numero_Identificacion = tAsesor.getValueAt(filaSeleccionada, 0).toString();
        String nombre = tAsesor.getValueAt(filaSeleccionada, 1).toString();
        String direccion = tAsesor.getValueAt(filaSeleccionada, 2).toString();
        String telefono = tAsesor.getValueAt(filaSeleccionada, 3).toString();
        String correo = tAsesor.getValueAt(filaSeleccionada, 4).toString();

        // Pasar los datos al formulario
        CreandoAsesorVista actualizarVista = new CreandoAsesorVista(this);
        actualizarVista.setVisible(true);
        actualizarVista.jbtAgregar.setEnabled(false);
        actualizarVista.cargarDatos(numero_Identificacion, nombre, direccion, telefono, correo);
            // TODO add your handling code here:

    }//GEN-LAST:event_btnActualizarActionPerformed

