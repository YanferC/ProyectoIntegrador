    public void llenarTablaAsesor(List<Asesor> listaAsesor) {
        // Crear el modelo de la tabla con las columnas definidas
        DefaultTableModel modelo = new DefaultTableModel(
                new String[]{
                    "Número Identificación", "Nombre Completo", 
                    "Dirección", "Telefono", "Correo Electronico"
                }, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;  // Bloquear edición de las celdas
            }
        };

        // Agregar los datos al modelo
        for (Asesor asesor : listaAsesor) {
            Object[] fila = {
                asesor.getNumero_Identificacion(),
                asesor.getNombre_Completo(),
                asesor.getDireccion(),
                asesor.getTelefono(),
                asesor.getCorreo_Electronico()
            };
            modelo.addRow(fila);
        }

        tAsesor.setModel(modelo);
        tAsesor.setBackground(Color.decode("#AFE5EF"));
        tAsesor.setForeground(Color.BLACK);             // Texto en negro
        tAsesor.setSelectionBackground(new Color(100, 150, 255)); // Fondo al seleccionar
        tAsesor.setSelectionForeground(Color.WHITE);    // Texto al seleccionar
    }

