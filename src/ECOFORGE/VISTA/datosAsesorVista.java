    public void actualizarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tAsesor.getModel();
        modelo.setRowCount(0); // Limpiar la tabla

        List<Asesor> listaAsesor = crearAsesor.armarCrud().ObtenerTodo();

         // Agregar los datos al modelo
        for (Asesor asesor : listaAsesor) {
            Object[] fila = {
                asesor.getNumero_Identificacion(),
                asesor.getNombre_Completo(),
                asesor.getDireccion(),
                asesor.getTelefono(),
                asesor.getCorreo_Electronico()
            };
            modelo.addRow(fila);
        }
    }

